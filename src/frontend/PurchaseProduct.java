    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(ssn.getText().equals("")||id.getText().equals("")||date.getDate()==null){
            JOptionPane.showMessageDialog(this, "some fields are empty");
        }
        else if(!er.contain(id.getText())){
            JOptionPane.showMessageDialog(this, "product with the id ="+id.getText()+" doesn't exist");
        }
        else if(!er.purchaseProduct(ssn.getText(), id.getText(), date.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate())){
            JOptionPane.showMessageDialog(this, "all items of the product id ="+id.getText()+" have been finished and no item is left for the customer");
        }
        else{
            JOptionPane.showMessageDialog(this, "the customer with SSN ="+ssn.getText()+" has successfully purchased the product with id ="+id.getText());
            ssn.setText("");
            id.setText("");
            date.setDate(null);
//            this.setVisible(false);
//            ((JFrame)getparentnode()).setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

